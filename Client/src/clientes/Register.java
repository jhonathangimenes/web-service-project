    public boolean fieldVerification(){
        
        try{
            Integer.parseInt(gross_income.getText());
        }catch(Exception e){
            return false;
        }
        return true;
    }
    
    public int registrationVerification(){
        
        int permission = 0;
        if(fieldVerification()){
            
            if(name.getText().trim().isEmpty() || last_name.getText().trim().isEmpty()
                    || office.getText().trim().isEmpty() || gross_income.getText().trim().isEmpty()){
                permission = 2;
            }
            
            if(Integer.parseInt(gross_income.getText()) > 0){
                permission = 1;
            }
        }
        return permission;
    }
    
    public void register()throws RemoteException, NotBoundException{
        
        listEmployee = getEmployee();
        
        if(registrationVerification() == 1){
            
            int registration  = 101;

            double income = Float.parseFloat(gross_income.getText());

            double margin = income*35 / 100;

            if(listEmployee.size() == 0){
                Employee e = new Employee();
                e.setRegistration(registration);
                e.setName(name.getText());
                e.setLastName(last_name.getText());
                e.setOffice(office.getText());
                e.setGrossIncome(Float.parseFloat(gross_income.getText()));
                e.setConsignableMargin(margin);

                addEmployee(e);
                new RegistratioMessage().setVisible(true);
            }else{
                int tie = listEmployee.size()+1;
                for(int i = 0; i < tie; i++){
                    if(listEmployee.size() == i){
                        Employee e = new Employee();
                        e.setRegistration(registration);
                        e.setName(name.getText());
                        e.setLastName(last_name.getText());
                        e.setOffice(office.getText());
                        e.setGrossIncome(Float.parseFloat(gross_income.getText()));
                        e.setConsignableMargin(margin);

                        addEmployee(e);
                        new RegistratioMessage().setVisible(true);
                    }else{
                        registration++;
                    }
                }
            }
        }else if(registrationVerification() == 2){
            new MessageNotExist().setVisible(true);
        }else{
            new IncorrectFieldMessage().setVisible(true);
        }
    }
